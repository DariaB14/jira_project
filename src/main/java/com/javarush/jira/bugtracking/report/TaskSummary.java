package com.javarush.jira.bugtracking.report;

public record TaskSummary(String status, long total) {
}

/*
record, введенный в Java 16. Это неизменяемый (immutable) класс, который
предназначен для хранения данных. В данном случае он используется для представления
сводной статистики по задачам.

Record автоматически генерирует для вас следующее:
private final поля для каждого компонента.
Канонический конструктор, принимающий все компоненты в порядке объявления.
Геттеры с именами status() и total() (без префикса get).
Методы equals(), hashCode(), toString().

TaskSummary предназначен для передачи агрегированных данных —
результатов группировки и подсчета задач по статусам.
 */